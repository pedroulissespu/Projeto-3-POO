    private void botaoEnviarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_botaoEnviarActionPerformed
        String titulo = inserirNome.getText();
        String atores = "";
        String genero = "";
        String classificacao = "";
        String preco = "";
        String dadosAntigos = titulo + "\t";

        File temp = new File(Manipulacao.DB_FILMES + "filmes.txt");
        String[] lines = Manipulacao.lerArquivo(temp).split("\n");

        for (String line : lines) {
            String aux = line.split("\t")[0] + "\t";
            if (aux.equals(dadosAntigos)) {
                // 0 = titulo , ja tem o titulo
                atores = line.split("\t")[1];
                genero = line.split("\t")[2];
                classificacao = line.split("\t")[3];
                preco = line.split("\t")[4];
                break;
            }
        }

        String tituloNovo = inserirNovoTitulo.getText();
        String atoresNovo = inserirNovosAtores.getText();
        String generoNovo = inserirNovoGenero.getText();
        String classificacaoNovo = inserirNovaClassificacao.getText();
        String precoNovo = inserirNovoPreco.getText();
        if (inserirNovoTitulo.getText().equals("")) {
            tituloNovo = titulo;
        }
        if (inserirNovosAtores.getText().equals("")) {
            atoresNovo = atores;
        }
        if (inserirNovoGenero.getText().equals("")) {
            generoNovo = genero;
        }
        if (inserirNovaClassificacao.getText().equals("")) {
            classificacaoNovo = classificacao;
        }
        if (inserirNovoPreco.getText().equals("")) {
            precoNovo = preco;
        }
        String dadosNovos = tituloNovo + "\t" + atoresNovo + "\t" + generoNovo + "\t" + classificacaoNovo + "\t"
                + precoNovo + "\n";

        File filme = new File(Manipulacao.DB_FILMES + "filmes.txt");

        try {
            Manipulacao.alterarDadosArquivoFilme(filme, dadosAntigos, dadosNovos);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "ERRO");
        }

        JOptionPane.showMessageDialog(rootPane, "Dados do filme " + tituloNovo + " Alterados com sucesso !");
        dispose();
    }// GEN-LAST:event_botaoEnviarActionPerformed

