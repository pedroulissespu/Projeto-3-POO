    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_botaoPesquisarActionPerformed
        String nome = inserirNome.getText();

        String pesquisa = nome + "\t";
        String preencher = "";
        File filme = new File(Manipulacao.DB_FILMES + "filmes.txt");

        String[] lines = Manipulacao.lerArquivo(filme).split("\n");

        for (String line : lines) {
            String aux = line.split("\t")[0] + "\t";
            if (aux.equals(pesquisa)) {
                preencher = line.split("\t")[0] + "\t" + line.split("\t")[1] + "\t" + line.split("\t")[2] + "\t"
                        + line.split("\t")[3] + "\t" + line.split("\t")[4] + "\n";
                break;
            }
        }

        DefaultTableModel modelo = (DefaultTableModel) mostrarFilme.getModel();
        if (modelo.getRowCount() > 0) {
            modelo.setRowCount(0);
        }

        Object[] obj = preencher.split("\t");
        modelo.addRow(obj);
    }// GEN-LAST:event_botaoPesquisarActionPerformed

